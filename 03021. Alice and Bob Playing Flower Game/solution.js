/**
 * Calculates half the product of n and m, rounded down
 *
 * @param {number} n - The first number
 * @param {number} m - The second number
 *
 * @returns {number} - The calculated value
 */
const flowerGame = (n, m) => Math.floor((n * m) / 2)
